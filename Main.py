print("Завдання 2: Дробова частина числа")
x = input("Введіть дійсне додатне число: ")
dot = x.find('.')
print("Результат:", '0' + x[dot:] if dot != -1 else '0')
print("=" * 40)

print("Завдання 3: Переведення секунд у дні, години, хвилини, секунди")
s = int(input("Введіть кількість секунд: "))
d = s // 86400
h = (s % 86400) // 3600
m = (s % 3600) // 60
s = s % 60
print(f"Результат: {d} days, {h} hours, {m} minutes, {s} seconds")
print("=" * 40)

print("Завдання 4: Ціна за n книг")
a = int(input("Гривні за книгу: "))
b = int(input("Копійки за книгу: "))
n = int(input("Кількість книг: "))
total_kop = (a * 100 + b) * n
print("Результат:", total_kop // 100, total_kop % 100)
print("=" * 40)

print("Завдання 5: Обмін значень без умов")
x = input("Введіть 0/1 або 3/7 або a/b: ")
d = '10' + '37' + 'ab'
i = d.find(x)
print("Результат:", d[i ^ 1])
print("=" * 40)

print("Завдання 6: Округлення дробового числа")
x = input("Введіть число з плаваючою крапкою: ")
dot = x.find('.')
decimal = x[dot+1:dot+2] + '0'
print("Результат:", int(x[:dot]) + (int(decimal) + 5) // 10)
print("=" * 40)

print("Завдання 7: Наступне парне число")
n = int(input("Введіть невід'ємне ціле число: "))
print("Результат:", n + 2 - n % 2)
print("=" * 40)

print("Завдання 8: День коли лінивець дістанеться вершини")
a = int(input("Висота підйому за день (a): "))
b = int(input("Сповзання за ніч (b): "))
h = int(input("Висота дерева (h): "))
